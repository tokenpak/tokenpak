    """
    Calculate the total price of all items in the shopping cart.

    This function iterates over each item in the provided list and
    sums up the price * quantity for each item.

    Args:
        items: A list of item dictionaries with 'price' and 'qty' keys.

    Returns:
        The total price as a float.
    """
    # Initialize total
    total = 0.0
    # Loop over items
    for item in items:
        total += item["price"] * item["qty"]
    return total
