"""
TokenPak Recipe SDK — Custom recipe development tooling.

Provides scaffolding, validation, testing, and benchmarking for
custom compression recipes (YAML-based declarative format).

Usage (CLI layer in cli.py):
    tokenpak recipe create <name>
    tokenpak recipe test   <file>
    tokenpak recipe validate <file>
    tokenpak recipe benchmark <file>

Programmatic usage::

    from tokenpak.agent.recipe_sdk import RecipeSDK
    sdk = RecipeSDK()
    sdk.validate("my_recipe.yaml")
    result = sdk.test("my_recipe.yaml", input_text="...")
    bench  = sdk.benchmark("my_recipe.yaml", samples=[...])
"""

from __future__ import annotations

import json
import re
import time
from pathlib import Path
from typing import Any

import yaml

# ── Schema ────────────────────────────────────────────────────────────────────

RECIPE_SCHEMA: dict[str, Any] = {
    "required_top_level": ["name", "category", "description", "pattern", "action"],
    "pattern_keys": {
        "required": ["match"],
        "match_modes": ["any", "extension", "filename", "content", "path_pattern"],
        "match_mode_keys": {
            "extension": ["extensions"],
            "filename": ["filenames"],
            "content": ["keywords"],
            "path_pattern": ["path_patterns"],
        },
    },
    "action_keys": {
        "required": ["operations"],
        "optional": ["compression_hint"],
        "compression_hint_range": (0.0, 1.0),
    },
    "known_operation_types": [
        "regex_replace",
        "strip_comments",
        "deduplicate_lines",
        "truncate_lines",
        "remove_empty_lines",
        "python_docstring_compress",
        "collapse_whitespace",
        "remove_filler_phrases",
        "json_compact",
        "keyword_filter",
    ],
    "known_categories": [
        "python",
        "javascript",
        "typescript",
        "markdown",
        "yaml",
        "json",
        "sql",
        "html",
        "css",
        "general",
        "legal",
        "medical",
        "config",
        "logs",
        "git",
    ],
}

