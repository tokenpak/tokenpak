"""Phase PI-2 — Claude Code Companion preview read model.

Read-only join over the four Intent Layer tables:

  - ``intent_events`` (Phase 0)
  - ``intent_policy_decisions`` (Phase 2.1)
  - ``intent_suggestions`` (Phase 2.4.1)
  - ``intent_patches`` (PI-1)

Surfaces the latest Claude-Code-style preview payload with three
identity-separation labels:

  - ``source_client = "claude_code"`` — the *client identity* that
    produced this traffic.
  - ``format_adapter = "AnthropicAdapter"`` — the *wire format
    identity* the request matched. There is no separate
    ``ClaudeCodeAdapter``; Claude Code traffic uses
    AnthropicAdapter because the wire shape is Anthropic Messages.
  - ``credential_provider = "tokenpak-claude-code"`` — when the
    Phase 0 ``ClaudeCodeCredentialProvider`` resolved the route.

And the wire-emission posture:

  - ``wire_emission = "telemetry_only"`` — pinned by the Phase 0
    §4.3 capability gate. AnthropicAdapter does NOT declare
    ``tip.intent.contract-headers-v1``; therefore no TIP intent
    headers ever land on the wire for Claude Code traffic.

This module is **read-only**. It never writes to any of the four
underlying tables; never invokes a provider; never mutates any
request. Mirrors the privacy contract from PI-1: structured
fields + templated text only; no raw prompt content.

PI-2 detection model
--------------------

PI-2 chooses an "ask-side" detection model: the operator
explicitly invokes the ``tokenpak claude-code`` CLI (or queries
the equivalent read model). The label
``source_client = "claude_code"`` is added by *this read model*
because the operator specifically asked for the Claude-Code-
shaped view. We do not expand the ``intent_events`` schema with
a per-row ``source_client`` column in PI-2; that's deferred to a
later sub-phase if/when wire-side header emission lands.

This means: a row that *was* generated by non-Claude-Code traffic
will still show as ``source_client = "claude_code"`` if the
operator runs ``tokenpak claude-code intent --last`` against it.
The CLI render text makes this clear: the surface labels the
view, not a per-row classification.
"""

from __future__ import annotations

import json
import sqlite3
from pathlib import Path
from typing import Any, Dict, Optional

SOURCE_CLIENT_CLAUDE_CODE: str = "claude_code"
FORMAT_ADAPTER_ANTHROPIC: str = "AnthropicAdapter"
CREDENTIAL_PROVIDER_CLAUDE_CODE: str = "tokenpak-claude-code"
WIRE_EMISSION_TELEMETRY_ONLY: str = "telemetry_only"

PREVIEW_LABELS = (
    "Claude Code Companion Intent Preview",
    "PREVIEW ONLY",
    "NOT APPLIED",
    "NO PROMPT MUTATION",
    "NO CLAUDE CODE INJECTION YET",
    "Telemetry-only; no TIP intent headers emitted",
)


def _intent_db_path() -> Path:
    """Resolved telemetry.db path. Mirrors :mod:`intent_contract`."""
    from tokenpak.proxy.intent_contract import _DEFAULT_DB_PATH

    return _DEFAULT_DB_PATH


def _table_exists(conn: sqlite3.Connection, name: str) -> bool:
    row = conn.execute(
        "SELECT 1 FROM sqlite_master WHERE type='table' AND name=?",
        (name,),
    ).fetchone()
    return row is not None


def collect_latest_preview(
    *, db_path: Optional[Path] = None
) -> Optional[Dict[str, Any]]:
    """Return the latest Claude-Code-shaped preview payload.

    Joins the four Intent Layer tables on the latest classification.
    Returns ``None`` when no rows exist anywhere; otherwise returns
    a dict with the directive-mandated 18 fields.

    Read-only. Tolerates any missing table (Phase 0 only / Phase 0
    + 2.1 only / Phase 0 + 2.1 + 2.4.1 only / etc) — uses ``None``
    for sections that haven't been written.
    """
    path = db_path if db_path is not None else _intent_db_path()
    if not path.is_file():
        return None
    try:
        with sqlite3.connect(str(path)) as conn:
            conn.row_factory = sqlite3.Row

            # Phase 0 — intent_events. Required; bail if not
            # present.
            if not _table_exists(conn, "intent_events"):
                return None
            event = conn.execute(
                "SELECT request_id, contract_id, timestamp, "
                "raw_prompt_hash, intent_class, intent_confidence, "
                "intent_slots_present, intent_slots_missing, "
                "intent_source, catch_all_reason, "
                "tip_headers_emitted, tip_headers_stripped "
                "FROM intent_events "
                "ORDER BY timestamp DESC LIMIT 1"
            ).fetchone()
            if event is None:
                return None

            # Phase 2.1 — intent_policy_decisions. Optional.
            decision: Optional[Dict[str, Any]] = None
            if _table_exists(conn, "intent_policy_decisions"):
                d = conn.execute(
                    "SELECT decision_id, contract_id, timestamp, "
                    "mode, intent_class, intent_confidence, "
                    "action, decision_reason, safety_flags "
                    "FROM intent_policy_decisions "
                    "WHERE contract_id = ? "
                    "ORDER BY timestamp DESC LIMIT 1",
                    (event["contract_id"],),
                ).fetchone()
                if d is not None:
                    try:
                        flags = json.loads(d["safety_flags"] or "[]")
                    except (TypeError, json.JSONDecodeError):
                        flags = []
                    decision = {
                        "decision_id": d["decision_id"],
                        "timestamp": d["timestamp"],
                        "mode": d["mode"],
                        "intent_class": d["intent_class"],
                        "intent_confidence": d["intent_confidence"],
                        "action": d["action"],
                        "decision_reason": d["decision_reason"],
                        "safety_flags": flags,
                    }

            # Phase 2.4.1 — intent_suggestions. Optional.
            suggestion: Optional[Dict[str, Any]] = None
            if (
                decision is not None
                and _table_exists(conn, "intent_suggestions")
            ):
                s = conn.execute(
                    "SELECT suggestion_id, decision_id, contract_id, "
                    "timestamp, suggestion_type, title, message, "
                    "recommended_action, confidence, safety_flags, "
                    "requires_confirmation, user_visible, expires_at, "
                    "source FROM intent_suggestions "
                    "WHERE decision_id = ? "
                    "ORDER BY timestamp DESC LIMIT 1",
                    (decision["decision_id"],),
                ).fetchone()
                if s is not None:
                    try:
                        sflags = json.loads(s["safety_flags"] or "[]")
                    except (TypeError, json.JSONDecodeError):
                        sflags = []
                    suggestion = {
                        "suggestion_id": s["suggestion_id"],
                        "timestamp": s["timestamp"],
                        "suggestion_type": s["suggestion_type"],
                        "title": s["title"],
                        "message": s["message"],
                        "recommended_action": s["recommended_action"],
                        "confidence": s["confidence"],
                        "safety_flags": sflags,
                        "requires_confirmation": bool(
                            s["requires_confirmation"]
                        ),
                        "user_visible": bool(s["user_visible"]),
                        "expires_at": s["expires_at"],
                        "source": s["source"],
                    }

            # PI-1 — intent_patches. Optional.
            patch: Optional[Dict[str, Any]] = None
            if (
                suggestion is not None
                and _table_exists(conn, "intent_patches")
            ):
                p = conn.execute(
                    "SELECT patch_id, contract_id, decision_id, "
                    "suggestion_id, created_at, mode, target, "
                    "original_hash, patch_text, reason, "
                    "confidence, safety_flags, "
                    "requires_confirmation, applied, source "
                    "FROM intent_patches "
                    "WHERE suggestion_id = ? "
                    "ORDER BY created_at DESC LIMIT 1",
                    (suggestion["suggestion_id"],),
                ).fetchone()
                if p is not None:
                    try:
                        pflags = json.loads(p["safety_flags"] or "[]")
                    except (TypeError, json.JSONDecodeError):
                        pflags = []
                    patch = {
                        "patch_id": p["patch_id"],
                        "contract_id": p["contract_id"],
                        "decision_id": p["decision_id"],
                        "suggestion_id": p["suggestion_id"],
                        "created_at": p["created_at"],
                        "mode": p["mode"],
                        "target": p["target"],
                        "original_hash": p["original_hash"],
                        "patch_text": p["patch_text"],
                        "reason": p["reason"],
                        "confidence": p["confidence"],
                        "safety_flags": pflags,
                        "requires_confirmation": bool(
                            p["requires_confirmation"]
                        ),
                        "applied": bool(p["applied"]),
                        "source": p["source"],
                    }
    except sqlite3.DatabaseError:
        return None

    return {
        # Identity-separation labels (PI-2 directive § 3)
        "source_client": SOURCE_CLIENT_CLAUDE_CODE,
        "format_adapter": FORMAT_ADAPTER_ANTHROPIC,
        "credential_provider": CREDENTIAL_PROVIDER_CLAUDE_CODE,
        "wire_emission": WIRE_EMISSION_TELEMETRY_ONLY,
        # Section payloads
        "event": _event_to_dict(event),
        "decision": decision,
        "suggestion": suggestion,
        "patch": patch,
        # Directive-mandated label set
        "labels": list(PREVIEW_LABELS),
    }


def _event_to_dict(event: sqlite3.Row) -> Dict[str, Any]:
    try:
        slots_present = json.loads(event["intent_slots_present"] or "[]")
    except (TypeError, json.JSONDecodeError):
        slots_present = []
    try:
        slots_missing = json.loads(event["intent_slots_missing"] or "[]")
    except (TypeError, json.JSONDecodeError):
        slots_missing = []
    return {
        "request_id": event["request_id"],
        "contract_id": event["contract_id"],
        "timestamp": event["timestamp"],
        "raw_prompt_hash": event["raw_prompt_hash"],
        "intent_class": event["intent_class"],
        "intent_confidence": event["intent_confidence"],
        "intent_slots_present": slots_present,
        "intent_slots_missing": slots_missing,
        "intent_source": event["intent_source"],
        "catch_all_reason": event["catch_all_reason"],
        "tip_headers_emitted": bool(event["tip_headers_emitted"]),
        "tip_headers_stripped": bool(event["tip_headers_stripped"]),
    }


def collect_latest_patch_preview(
    *, db_path: Optional[Path] = None
) -> Optional[Dict[str, Any]]:
    """Return the latest patch row tagged with Claude-Code-shape labels.

    Distinct from :func:`collect_latest_preview` in that this
    returns ONLY the patch payload + the identity labels, not the
    full intent_events / decision / suggestion chain. Used by
    ``tokenpak claude-code patches --last``.
    """
    from tokenpak.proxy.intent_prompt_patch_telemetry import (
        IntentPatchStore,
        get_default_patch_store,
    )

    if db_path is not None:
        store = IntentPatchStore(db_path=db_path)
    else:
        store = get_default_patch_store()
    p = store.fetch_latest()
    if p is None:
        return None
    return {
        "source_client": SOURCE_CLIENT_CLAUDE_CODE,
        "format_adapter": FORMAT_ADAPTER_ANTHROPIC,
        "credential_provider": CREDENTIAL_PROVIDER_CLAUDE_CODE,
        "wire_emission": WIRE_EMISSION_TELEMETRY_ONLY,
        "patch": p,
        "labels": list(PREVIEW_LABELS),
    }


__all__ = [
    "CREDENTIAL_PROVIDER_CLAUDE_CODE",
    "FORMAT_ADAPTER_ANTHROPIC",
    "PREVIEW_LABELS",
    "SOURCE_CLIENT_CLAUDE_CODE",
    "WIRE_EMISSION_TELEMETRY_ONLY",
    "collect_latest_patch_preview",
    "collect_latest_preview",
]
