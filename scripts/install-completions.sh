# ── Install fish completion ────────────────────────────────────────────────────
install_fish() {
    local dest_dirs=(
        "/usr/share/fish/vendor_completions.d"
        "${HOME}/.config/fish/completions"
    )

    local dest=""
    for d in "${dest_dirs[@]}"; do
        if [[ -d "$d" ]] || [[ "$d" == "${HOME}/.config/fish/completions" ]]; then
            dest="$d/tokenpak.fish"
            break
        fi
    done

    echo "  📁 Fish destination: ${dest}"

    if $DRY_RUN; then
        echo "  [dry-run] Would write fish completion to ${dest}"
        return
    fi

    mkdir -p "$(dirname "${dest}")"

    cat > "${dest}" << 'FISH_COMPLETION'
# TokenPak fish completion
# Generated by: tokenpak scripts/install-completions.sh

set -l commands activate agent audit benchmark budget calibrate compliance compression config cost dashboard deactivate debug demo diff doctor fingerprint handoff help index last license lock logs macro maintenance metrics optimize plan policy prune recipe replay restart retain route run savings search serve sla start stats status stop template trigger update validate vault version workflow

complete -c tokenpak -f -n "not __fish_seen_subcommand_from $commands" -a "$commands"

# Global options
complete -c tokenpak -l help -s h -d "Show help"

# Command-specific options
complete -c tokenpak -n "__fish_seen_subcommand_from start serve" -l port -d "Port to listen on"
complete -c tokenpak -n "__fish_seen_subcommand_from start serve" -l workers -d "Number of workers"
complete -c tokenpak -n "__fish_seen_subcommand_from status" -l json -d "Output as JSON"
complete -c tokenpak -n "__fish_seen_subcommand_from cost savings metrics" -l period -d "Time period (DAY|WEEK|MONTH)"
complete -c tokenpak -n "__fish_seen_subcommand_from cost savings metrics" -l json -d "Output as JSON"
complete -c tokenpak -n "__fish_seen_subcommand_from index" -l status -d "Show indexed file count"
complete -c tokenpak -n "__fish_seen_subcommand_from index" -l workers -d "Number of workers"
complete -c tokenpak -n "__fish_seen_subcommand_from search" -l limit -d "Number of results"
complete -c tokenpak -n "__fish_seen_subcommand_from doctor" -l fix -d "Auto-fix issues"
complete -c tokenpak -n "__fish_seen_subcommand_from doctor" -l fleet -d "Check all fleet agents"
complete -c tokenpak -n "__fish_seen_subcommand_from benchmark" -l file -d "Benchmark a specific file"
complete -c tokenpak -n "__fish_seen_subcommand_from benchmark" -l sample -d "Use sample data"
complete -c tokenpak -n "__fish_seen_subcommand_from benchmark" -l json -d "Output as JSON"
complete -c tokenpak -n "__fish_seen_subcommand_from budget" -l limit -d "Budget limit amount"
complete -c tokenpak -n "__fish_seen_subcommand_from update" -l check -d "Check without installing"
complete -c tokenpak -n "__fish_seen_subcommand_from prune" -l older-than -d "Days threshold"
complete -c tokenpak -n "__fish_seen_subcommand_from prune" -l dry-run -d "Preview only"
complete -c tokenpak -n "__fish_seen_subcommand_from dashboard" -l json -d "Export as JSON"
complete -c tokenpak -n "__fish_seen_subcommand_from dashboard" -l fleet -d "Fleet-wide summary"
complete -c tokenpak -n "__fish_seen_subcommand_from help" -l more -d "Show intermediate commands"
complete -c tokenpak -n "__fish_seen_subcommand_from help" -l all -d "Show all commands"
complete -c tokenpak -n "__fish_seen_subcommand_from help" -l minimal -d "Compact list"
FISH_COMPLETION

    echo "  ✅ Fish completion installed to: ${dest}"
}

# ── Run installations ──────────────────────────────────────────────────────────
echo ""
echo "Installing TokenPak shell completions..."
echo ""

case "${CURRENT_SHELL}" in
    bash)
        install_bash
        ;;
    zsh)
        install_zsh
        ;;
    fish)
        install_fish
        ;;
    *)
        echo "⚠️  Unknown shell: ${CURRENT_SHELL}. Installing for bash and zsh..."
        install_bash
        install_zsh
        ;;
esac

echo ""
if $DRY_RUN; then
    echo "✅ Dry-run complete. No files were written."
else
    echo "✅ Completions installed!"
    echo ""
    echo "To activate in current session:"
    case "${CURRENT_SHELL}" in
        bash)   echo "  source ~/.bashrc" ;;
        zsh)    echo "  exec zsh" ;;
        fish)   echo "  (completions load automatically)" ;;
        *)      echo "  Restart your shell or source your rc file." ;;
    esac
fi
echo ""
